id int,
Manufacturer Varchar(30),
Model Varchar(30),
Transmission Varchar(30),
Color Varchar(30),
Odometer int,
Year int, 
Engine_Type Varchar(30),
Engine_Capacity float,
Body Varchar(30),
State Varchar(30),
Drivetrain Varchar(30),
Price float,
List_Duration int
);

